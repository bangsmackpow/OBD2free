export function getDTCInfo(code: string): DTCInfo | null {
  return dtcDatabase[code.toUpperCase()] || null;
}

export function searchDTCs(query: string): DTCInfo[] {
  const q = query.toLowerCase();
  return Object.values(dtcDatabase).filter(
    (dtc) =>
      dtc.code.toLowerCase().includes(q) ||
      dtc.description.toLowerCase().includes(q) ||
      dtc.category.toLowerCase().includes(q) ||
      dtc.causes.some((c) => c.toLowerCase().includes(q)) ||
      dtc.symptoms.some((s) => s.toLowerCase().includes(q)),
  );
}

export function getDTCsByCategory(category: string): DTCInfo[] {
  return Object.values(dtcDatabase).filter((dtc) => dtc.category === category);
}

export function getDTCCategories(): string[] {
  const cats = new Set(Object.values(dtcDatabase).map((d) => d.category));
  return Array.from(cats).sort();
}

export default dtcDatabase;
